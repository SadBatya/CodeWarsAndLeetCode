// You have a long flowerbed in which some of the plots are planted, and some are not. However, flowers cannot be planted in adjacent plots.

// Given an integer array flowerbed containing 0's and 1's, where 0 means empty and 1 means not empty, and an integer n, return true if n new flowers can be planted in the flowerbed without violating the no-adjacent-flowers rule and false otherwise.

// flowerbed = [1,0,0,0,1], n = 1
// Output: true

// flowerbed = [1,0,0,0,1], n = 1
// Output: false

// ============================================
// РЕШЕНИЕ:
// Проходим по массиву и проверяем каждый участок. Цветок можно посадить,
// если текущий участок пуст (0) и оба соседних участка пусты или находятся
// на границах массива. Если нашли n мест, возвращаем true.
// ============================================
// ВРЕМЕННАЯ СЛОЖНОСТЬ: O(n) - один проход по массиву
// ПРОСТРАНСТВЕННАЯ СЛОЖНОСТЬ: O(1) - используем только константное количество переменных
// ============================================

var canPlaceFlowers = function (flowerbed, n) {
  // Шаг 1: Если не нужно сажать цветы, возвращаем true
  if (n === 0) {
    return true;
  }

  // Шаг 2: Счетчик посаженных цветов
  let flowerCount = 0;
  
  // Шаг 3: Проходим по массиву
  for (let i = 0; i < flowerbed.length; i++) {
    // Шаг 4: Проверяем, пуст ли левый сосед (или это начало массива)
    let leftEmpty = i === 0 || flowerbed[i - 1] === 0;
    // Шаг 5: Проверяем, пуст ли правый сосед (или это конец массива)
    let rightEmpty = i === flowerbed.length - 1 || flowerbed[i + 1] === 0;

    // Шаг 6: Если текущий участок пуст и оба соседа пусты
    if (flowerbed[i] === 0 && leftEmpty && rightEmpty) {
      // Шаг 7: Сажаем цветок и увеличиваем счетчик
      flowerCount++;
      flowerbed[i] = 1;
      
      // Шаг 8: Если посадили достаточно цветов, возвращаем true
      if (flowerCount >= n) {
        return true;
      }
    }
  }

  // Шаг 9: Если не удалось посадить n цветов, возвращаем false
  return false;
};

console.log(canPlaceFlowers([1, 0, 0, 0, 1], 1)); // true
console.log(canPlaceFlowers([1, 0, 0, 0, 1], 2)); // false


