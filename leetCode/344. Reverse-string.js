// 344. Reverse String
// while function that reverses the string in-place using two pointers
/**
 * @param {character[]} s
 * @return {void} Do not return anything, modify s in-place instead.
 */

// ============================================
// РЕШЕНИЕ:
// Используем технику двух указателей. Меняем местами символы с начала и конца,
// постепенно двигаясь к центру массива.
// ============================================
// ВРЕМЕННАЯ СЛОЖНОСТЬ: O(n) - один проход по половине массива
// ПРОСТРАНСТВЕННАЯ СЛОЖНОСТЬ: O(1) - используем только константное количество переменных
// ============================================

const reverseString = (s) => {
  // Шаг 1: Инициализируем два указателя: слева и справа
  let left = 0;
  let right = s.length - 1;

  // Шаг 2: Продолжаем пока указатели не встретятся
  while (left < right) {
    // Шаг 3: Меняем местами символы на позициях left и right
    [s[left], s[right]] = [s[right], s[left]];

    // Шаг 4: Двигаем указатели к центру
    left++;
    right--;
  }

  // Шаг 5: Массив модифицирован на месте
  return s;
};

