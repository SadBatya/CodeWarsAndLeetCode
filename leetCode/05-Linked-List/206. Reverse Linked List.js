// Given the head of a singly linked list, reverse the list, and return the reversed list.

// Example 1:

// Input: head = [1,2,3,4,5]
// Output: [5,4,3,2,1]
// Example 2:

// Input: head = [1,2]

// ============================================
// РЕШЕНИЕ:
// Используем итеративный подход с двумя указателями (prev и current).
// На каждой итерации сохраняем ссылку на следующий узел, меняем направление связи,
// и двигаем указатели вперед. В конце prev станет новой головой списка.
// ============================================
// ВРЕМЕННАЯ СЛОЖНОСТЬ: O(n) - один проход по списку
// ПРОСТРАНСТВЕННАЯ СЛОЖНОСТЬ: O(1) - используем только константное количество переменных
// ============================================

var reverseList = function (head) {
  // Шаг 1: Предыдущий узел (изначально null, так как у первого узла нет предыдущего)
  let prev = null;
  // Шаг 2: Текущий узел (начинаем с головы списка)
  let current = head;

  // Шаг 3: Продолжаем пока не дойдем до конца списка
  while (current) {
    // Шаг 4: Сохраняем ссылку на следующий узел (чтобы не потерять)
    let tmp = current.next;
    // Шаг 5: Меняем направление связи - текущий узел теперь указывает на предыдущий
    current.next = prev;
    // Шаг 6: Предыдущий узел становится текущим
    prev = current;
    // Шаг 7: Текущий узел становится следующим (который мы сохранили)
    current = tmp;
  }

  // Шаг 8: Возвращаем prev - это новая голова перевернутого списка
  return prev;
};
